SELECT NOW()
	 , YEAR(NOW())
     , QUARTER(NOW())
     , DAY(NOW())
	 , DATEDIFF(NOW(), '2025-01-01')
     , TIMESTAMPDIFF(MONTH, NOW(), '2025-01-01')
     , ADDDATE(NOW(), INTERVAL 100 DAY)
     , ADDDATE(NOW(), INTERVAL 100 MONTH)
     , SUBDATE(NOW(), INTERVAL 100 DAY)
     , SUBDATE(NOW(), INTERVAL 100 MONTH)
     , LAST_DAY(NOW())
     , MONTHNAME(NOW())
     , WEEKDAY(NOW());