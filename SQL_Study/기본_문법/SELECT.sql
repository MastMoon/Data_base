-- 데이터 조회를 위한 기본 SELECT 문법 정리
-- ==============================================

USE 한빛무역; -- 사용할 데이터베이스 선택

-- 테이블의 모든 데이터를 조회 (모든 컬럼 포함)
SELECT * FROM 고객;

-- 특정 컬럼만 조회
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객;

-- 컬럼에 별칭(Alias) 적용하여 출력 (AS 사용)
SELECT 고객번호 AS ID, 담당자명 AS "담당자 이름", 마일리지 AS "포인트"
FROM 고객;
