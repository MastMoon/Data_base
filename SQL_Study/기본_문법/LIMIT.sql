-- 특정 개수의 행만 조회하는 LIMIT 문법 정리
-- ==============================================

USE 한빛무역; -- 사용할 데이터베이스 선택

-- 3개 행만 조회 (기본 사용법)
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
LIMIT 3;

-- 10행부터 시작하여 3개 행만 조회
SELECT *
FROM 고객
LIMIT 10, 3;  -- 10행부터 시작하여 3개의 고객 정보를 확인

-- 마일리지 많은 고객부터 상위 3명 조회 (3개 출력)
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
ORDER BY 마일리지 DESC
LIMIT 3;

