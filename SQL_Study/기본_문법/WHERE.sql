USE 한빛무역; -- 사용할 데이터베이스 선택

-- 특정 조건을 만족하는 데이터 조회
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
WHERE 도시 = '서울특별시';

-- 숫자 조건 비교 (마일리지가 100000 초과인 고객)
SELECT 고객번호, 담당자명, 마일리지
FROM 고객
WHERE 마일리지 > 100000;