-- 2주차 SQL 학습: WHERE 사용법 (2025.03.13)
-- 특정 조건에 맞는 데이터만 조회하는 WHERE 문법 정리
-- ==============================================

USE 한빛무역; -- 사용할 데이터베이스 선택

-- 1️⃣ 특정 조건을 만족하는 데이터 조회
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
WHERE 도시 = '서울특별시';

-- 2️⃣ 숫자 조건 비교 (마일리지가 5000 초과인 고객)
SELECT 고객번호, 담당자명, 마일리지
FROM 고객
WHERE 마일리지 > 5000;

-- 3️⃣ 다중 조건 (AND 연산자: 두 조건 모두 만족해야 함)
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
WHERE 도시 = '서울특별시' AND 마일리지 > 5000;

-- 4️⃣ 다중 조건 (OR 연산자: 하나라도 만족하면 출력)
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
WHERE 도시 = '서울특별시' OR 마일리지 > 5000;

-- 5️⃣ 특정 값 제외 (NOT 사용)
SELECT 고객번호, 담당자명, 도시, 마일리지
FROM 고객
WHERE 도시 != '서울특별시';

-- 6️⃣ 특정 범위 내의 값 조회 (BETWEEN 사용)
SELECT 고객번호, 담당자명, 마일리지
FROM 고객
WHERE 마일리지 BETWEEN 3000 AND 8000;

-- 7️⃣ 특정 값 목록과 일치하는 데이터 조회 (IN 사용)
SELECT 고객번호, 담당자명, 도시
FROM 고객
WHERE 도시 IN ('서울특별시', '부산광역시', '대구광역시');

-- 8️⃣ 특정 패턴과 일치하는 데이터 조회 (LIKE 사용)
SELECT 고객번호, 담당자명, 도시
FROM 고객
WHERE 담당자명 LIKE '김%';  -- '김'으로 시작하는 이름

SELECT 고객번호, 담당자명, 도시
FROM 고객
WHERE 담당자명 LIKE '%철수'; -- '철수'로 끝나는 이름

SELECT 고객번호, 담당자명, 도시
FROM 고객
WHERE 담당자명 LIKE '%길%'; -- '길'이 포함된 이름

-- 9️⃣ NULL 값 조회 (IS NULL 사용)
SELECT 고객번호, 담당자명, 마일리지
FROM 고객
WHERE 마일리지 IS NULL;

-- 🔟 NULL 값이 아닌 데이터 조회 (IS NOT NULL 사용)
SELECT 고객번호, 담당자명, 마일리지
FROM 고객
WHERE 마일리지 IS NOT NULL;

/*
✅ WHERE 문법 정리:
- `WHERE 컬럼명 = '값';` → 특정 값을 가진 데이터만 조회
- `WHERE 컬럼명 != '값';` → 특정 값을 제외한 데이터 조회
- `WHERE 컬럼명 > 100;` → 특정 숫자보다 큰 값 조회
- `WHERE 컬럼명 BETWEEN X AND Y;` → 특정 범위 내의 값 조회
- `WHERE 컬럼명 IN ('값1', '값2', '값3');` → 특정 목록 내 값 조회
- `WHERE 컬럼명 LIKE '패턴';` → 특정 문자열 패턴 검색
- `WHERE 컬럼명 IS NULL;` → NULL 값 조회
- `WHERE 컬럼명 IS NOT NULL;` → NULL이 아닌 데이터 조회

⚠ `LIKE`에서 `_`는 한 글자를 의미하고 `%`는 여러 글자를 의미함!
*/

