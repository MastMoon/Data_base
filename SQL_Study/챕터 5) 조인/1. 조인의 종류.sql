---------------------------------------------------------
-- [CROSS JOIN 예제]
-- 부서와 사원 테이블의 모든 조합을 생성한 후,
-- 이름이 '배재용'인 사원만 필터링합니다.
SELECT 부서.부서번호,
       부서명,
       이름,
       사원.부서번호
FROM 부서
CROSS JOIN 사원
WHERE 이름 = '배재용';
 
/*
[예상 결과]
-----------------------------------------------------------
| 부서번호 | 부서명   | 이름    | 사원.부서번호              |
-----------------------------------------------------------
| 101      | 영업부   | 배재용  | 101                        |
| 102      | 기술부   | 배재용  | 102                        |
-----------------------------------------------------------
(예시: 부서와 사원을 CROSS JOIN 한 후, 이름이 '배재용'인 경우)
*/

---------------------------------------------------------
-- [INNER JOIN 예제 - ANSI 표준 구문]
-- 사원 테이블과 부서 테이블을 부서번호 기준으로 조인하여,
-- 이름이 '이소미'인 사원의 사원번호, 직위, 부서번호, 부서명을 조회합니다.
SELECT 사원번호,
       직위,
       사원.부서번호,
       부서명
FROM 사원
INNER JOIN 부서
ON 사원.부서번호 = 부서.부서번호
WHERE 이름 = '이소미';
 
/*
[예상 결과]
----------------------------------------------------------
| 사원번호 | 직위      | 부서번호  | 부서명                 |
----------------------------------------------------------
| 2001    | 대리      | 101       | 영업부                 |
----------------------------------------------------------
(예시: 이름이 '이소미'인 사원의 정보와 해당 부서 정보)
*/

