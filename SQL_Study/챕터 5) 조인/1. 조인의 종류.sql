---------------------------------------------------------
-- [집계 함수와 GROUP BY 예제: 주문 건수]
-- 고객과 주문 테이블을 조인하여, 
-- 각 고객별 주문 건수를 집계한 후 주문건수가 높은 순으로 정렬합니다.
SELECT 고객.고객번호, 
       담당자명, 
       고객회사명,
       COUNT(*) AS 주문건수
FROM 고객
INNER JOIN 주문
ON 고객.고객번호 = 주문.고객번호
GROUP BY 고객.고객번호,
         담당자명,
         고객회사명
ORDER BY 주문건수 DESC;
 
/*
[예상 결과]
------------------------------------------------------------------
| 고객번호 | 담당자명 | 고객회사명 | 주문건수                    |
------------------------------------------------------------------
| C001     | 홍길동   | ABC Corp  | 25                         |
| C002     | 김철수   | XYZ Inc   | 18                         |
| ...                                                       |
------------------------------------------------------------------
(예시: 각 고객별 주문 건수를 내림차순 정렬)
*/

---------------------------------------------------------
-- [집계 함수와 다중 JOIN 예제: 주문 금액 합계]
-- 고객, 주문, 주문세부 테이블을 조인하여,
-- 각 고객별로 (단가 * 주문수량)의 합계를 계산하여 주문금액합으로 출력합니다.
SELECT 고객.고객번호, 
       담당자명, 
       고객회사명,
       SUM(단가 * 주문수량) AS 주문금액합
FROM 고객 
INNER JOIN 주문
ON 고객.고객번호 = 주문.고객번호
INNER JOIN 주문세부
ON 주문.주문번호 = 주문세부.주문번호
GROUP BY 고객.고객번호,
         담당자명,
         고객회사명
ORDER BY 주문금액합 DESC;
 
/*
[예상 결과]
---------------------------------------------------------------------
| 고객번호 | 담당자명 | 고객회사명   | 주문금액합                  |
---------------------------------------------------------------------
| C005     | 이순신   | DEF Corp     | 500000                      |
| C001     | 홍길동   | ABC Corp     | 450000                      |
| ...                                                         |
---------------------------------------------------------------------
(예시: 주문금액합이 높은 순으로 정렬된 결과)
*/
